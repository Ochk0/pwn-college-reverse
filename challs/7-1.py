hex_array = hex_array[::-1]

hex_array.sort()

for i, val in enumerate(hex_array):
    t = i % 5
    # print(i, t)
    if t == 0:
        hex_array[i] = hex_array[i] ^ 0x3D
    if t == 1:
        hex_array[i] = hex_array[i] ^ 0x21
    if t == 2:
        hex_array[i] = hex_array[i] ^ 0x43
    if t == 3:
        hex_array[i] = hex_array[i] ^ 0xB8
    if t == 4:
        hex_array[i] = hex_array[i] ^ 0xE8

for i, val in enumerate(hex_array):
    print(chr(val), end='')